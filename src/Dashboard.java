    private void LogoutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LogoutMouseClicked
        // TODO add your handling code here:
       int logout = JOptionPane.showConfirmDialog(null, 
            "Do you want to logout from the system?\n\nUser: " + username + " (" + userRole + ")",
            "Confirm Logout",JOptionPane.YES_NO_OPTION);
        if(logout == 0) {
            // Clear session data
            loginPage.loggedInUsername = "";
            loginPage.loggedInUserRole = "";
            loginPage.loggedInFullName = "";
            loginPage.isLoggedIn = false;
            
            setVisible(false);
            new loginPage().setVisible(true);
        }
    }//GEN-LAST:event_LogoutMouseClicked

    private void AddUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddUserMouseClicked
        if (!hasAdminAccess()) {
            showAccessDenied("User Management");
            return;
        }
        
        setVisible(false);
        new AddUserPage().setVisible(true);
    }//GEN-LAST:event_AddUserMouseClicked

