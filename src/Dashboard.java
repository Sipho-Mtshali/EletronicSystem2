    public Dashboard() {
          // Get user data from loginPage static variables
        if (loginPage.isLoggedIn) {
            this.userRole = loginPage.loggedInUserRole;
            this.username = loginPage.loggedInUsername;
        } else {
            // Fallback - redirect to login
            this.dispose();
            new loginPage().setVisible(true);
            return;
        }
        initComponents();
        configureRoleBasedAccess();      
    }
    
        private void configureRoleBasedAccess() {
        // Update welcome message with user info
        jLabel1.setText("Welcome " + username + " (" + userRole + ")");
        
        if ("CLERK".equals(userRole)) {
            // Hide admin-only features for clerks
            AddUser.setVisible(false);  
            Customer.setVisible(false);         // User Management - Admin only
            Backup.setVisible(false);            // Database Backup - Admin only
            //Settings.setVisible(false);          // Settings - Admin only
            //AuditLogs.setVisible(false);         // Audit Logs - Admin only
            
            // Modify inventory access (view only for clerks)
            Inventory.setText("View Inventory");
            Inventory.setToolTipText("View-only access to inventory");
            
             // Add visual indicator for limited access
            Sales.setToolTipText("Limited sales access");
            //Customer.setToolTipText("Customer management access");
            
            // Adjust panel layout for fewer options
            adjustPanelLayoutForClerk();
            
               // Show a welcome message for clerks
            showClerkWelcomeMessage();
        } else if ("ADMIN".equals(userRole)) {
            // Full access for admin
            AddUser.setToolTipText("Full user management access");
            Backup.setToolTipText("Database backup and restore");
            Inventory.setToolTipText("Full inventory management");
            Sales.setToolTipText("Full sales management");
            Customer.setToolTipText("Full customer management");
        } 
    }
        
           /**
     * Show welcome message for clerk users
     */
    private void showClerkWelcomeMessage() {
        javax.swing.SwingUtilities.invokeLater(() -> {
            JOptionPane.showMessageDialog(this, 
                "Welcome, " + username + "!\n\n" +
                "You are logged in as a CLERK.\n" +
                "Available functions:\n" +
                "• Manage Customers\n" +
                "• View Inventory\n" +
                "• Manage Sales\n\n" +
                "Note: User management and backup functions are admin-only.",
                "Clerk Access", 
                JOptionPane.INFORMATION_MESSAGE);
        });
    }
    
    /**
     * Adjust panel layout when fewer options are visible (for clerks)
     */
    private void adjustPanelLayoutForClerk() {
        // Recalculate panel layout to accommodate hidden components
        jPanel1.revalidate();
        jPanel1.repaint();
    }
        /**
     * Check if user has admin privileges
     */
    private boolean hasAdminAccess() {
        return "Administrator".equals(userRole);
    }
    
    /**
     * Show access denied message
     */
    private void showAccessDenied(String feature) {
        JOptionPane.showMessageDialog(this, 
            "Access Denied!\n\n" +
            "You need ADMIN privileges to access: " + feature + "\n" +
            "Current role: " + userRole + "\n\n" +
            "Please contact your system administrator for access.",
            "Access Denied", 
            JOptionPane.WARNING_MESSAGE);
    }

