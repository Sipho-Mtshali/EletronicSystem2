    private void InventoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_InventoryMouseClicked
        setVisible(false);
        
        if (hasAdminAccess()) {
            // Admin gets full access
            new InventoryPage().setVisible(true);
        } else {
            // Clerk gets view-only access
            new InventoryPage().setVisible(true);
        }
    }//GEN-LAST:event_InventoryMouseClicked

    private void SalesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SalesMouseClicked
        // TODO add your handling code here:
        // Both clerks and admins can access sales, but with different permissions
        setVisible(false);
        
        if (hasAdminAccess()) {
            new SalesPage().setVisible(true);
        } else {
            // Clerks can make sales but may have limited access to reports/modifications
            new SalesPage().setVisible(true);
        }
    }//GEN-LAST:event_SalesMouseClicked

