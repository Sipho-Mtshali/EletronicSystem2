       // Method to load data from database into table
    private void uploadTableData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data
        
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ElectronicSystemDb", "root", "12345678");
            PreparedStatement ps = con.prepareStatement("SELECT * FROM customersTable");
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("fullname"),
                    rs.getString("email"),
                    rs.getString("contact_number")
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    // Method to setup table row selection listener
    private void setupTableSelection() {
        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int selectedRow = jTable1.getSelectedRow();
                if (selectedRow >= 0) {
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    
                    txtId.setText(model.getValueAt(selectedRow, 0).toString());
                    txtFullname.setText(model.getValueAt(selectedRow, 1).toString());
                    txtEmail.setText(model.getValueAt(selectedRow, 2).toString());
                    txtPhone.setText(model.getValueAt(selectedRow, 3).toString());
                }
            }
        });
    }
    
