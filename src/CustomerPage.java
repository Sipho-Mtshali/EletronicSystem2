    private void ButtonaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonaddActionPerformed
        // TODO add your handling code here:         // Get values from text fields
    String fullname = txtFullname.getText().trim();
    String email = txtEmail.getText().trim();
    String phone = txtPhone.getText().trim();

    // Validate input
    if (fullname.isEmpty() || email.isEmpty() || phone.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill in all fields before submitting.");
        return; // Exit the method if any field is empty
    }

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ElectronicSystemDb", "root", "12345678");
        PreparedStatement ps = con.prepareStatement("INSERT INTO customersTable(fullname,email,contact_number) VALUES (?,?,?)");
        ps.setString(1, fullname);
        ps.setString(2, email);
        ps.setString(3, phone);
        
        int result = ps.executeUpdate();
        
        if(result == 1){
            JOptionPane.showMessageDialog(null, "Customer added successfully!");

            // Clear fields
            txtId.setText("");   
            txtFullname.setText("");
            txtEmail.setText("");
            txtPhone.setText("");

            // Refresh table
            uploadTableData();
        } else {
            JOptionPane.showMessageDialog(null, "Customer failed to save");
        }

        con.close();
    } catch(Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_ButtonaddActionPerformed

    private void ButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonDeleteActionPerformed
        // TODO add your handling code here:
String id = txtId.getText();
    
    if(id.trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please select a customer to delete");
        return;
    }
    
    int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this customer?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION){
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ElectronicSystemDb","root","12345678");
            PreparedStatement ps = con.prepareStatement("DELETE FROM customersTable WHERE id = ?");
            ps.setString(1, id);
            
            int result = ps.executeUpdate();
            
            if(result > 0) {
                JOptionPane.showMessageDialog(null, "Customer deleted successfully!");
                
                // Clear fields
                txtFullname.setText("");
                txtEmail.setText("");
                txtPhone.setText("");
                txtId.setText("");
                
                // Refresh table
                uploadTableData();
            } else {
                JOptionPane.showMessageDialog(null, "No customer found with that ID");
            }
            
            con.close(); // Close connection
        }
        catch(HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_ButtonDeleteActionPerformed

